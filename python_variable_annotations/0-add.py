#!/usr/bin/env python3
"""
This module provides a simple addition function.

Intended for basic arithmetic operations or as
a utility function in larger program.
"""


def add(a: float, b: float) -> float:
    """Return the sum of a and b.

    Args:
        a (float): first number to add.
        b (float): The second number to add.

    Returns:
        The result of a + b.
    """
    return a + b
